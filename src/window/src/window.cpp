// ============================================================
// Stage 1: Read 256 samples from AXI-Stream input
// Converts axis_t to internal_t (strips sideband signals)
// ============================================================
static void read_input(hls::stream<axis_t>& in, hls::stream<internal_t>& buf) {
    READ_LOOP:
    for (int i = 0; i < WIN_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        axis_t word = in.read();
        buf.write(word.data);
    }
}

// ============================================================
// Stage 2: Apply Hamming window to real and imaginary components
// ============================================================
static void apply_window(hls::stream<internal_t>& in_buf, hls::stream<internal_t>& out_buf) {
    WINDOW_LOOP:
    for (int i = 0; i < WIN_SIZE; i++) {
        #pragma HLS PIPELINE II=1

        internal_t raw = in_buf.read();

        // Extract real (upper 16 bits) and imaginary (lower 16 bits)
        data_t real_in, imag_in;
        real_in.range() = raw.range(31, 16);
        imag_in.range() = raw.range(15, 0);

        // Multiply by Hamming coefficient (full-precision product)
        acc_t real_prod = real_in * HAMMING_COEFF[i];
        acc_t imag_prod = imag_in * HAMMING_COEFF[i];

        // Truncate back to output data type
        data_t real_out = (data_t)real_prod;
        data_t imag_out = (data_t)imag_prod;

        // Pack output data
        internal_t out_data;
        out_data.range(31, 16) = real_out.range();
        out_data.range(15, 0) = imag_out.range();

        out_buf.write(out_data);
    }
}

// ============================================================
// Stage 3: Write 256 windowed samples to AXI-Stream output
// Converts internal_t back to axis_t (adds sideband signals)
// ============================================================
static void write_output(hls::stream<internal_t>& buf, hls::stream<axis_t>& out) {
    WRITE_LOOP:
    for (int i = 0; i < WIN_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        axis_t word;
        word.data = buf.read();
        word.last = (i == WIN_SIZE - 1) ? 1 : 0;
        word.keep = -1;
        word.strb = -1;
        out.write(word);
    }
}

// ============================================================
// Top-Level Function: 256-Point Hamming Window
// ============================================================
void window(hls::stream<axis_t>& in, hls::stream<axis_t>& out) {
    // AXI-Stream interfaces for streaming complex samples
    #pragma HLS INTERFACE axis port=in
    #pragma HLS INTERFACE axis port=out
    // Free-running kernel — no start/done handshake
    #pragma HLS INTERFACE ap_ctrl_none port=return

    #pragma HLS DATAFLOW

    hls::stream<internal_t> buf1;
    hls::stream<internal_t> buf2;
    #pragma HLS STREAM variable=buf1 depth=256
    #pragma HLS STREAM variable=buf2 depth=256

    read_input(in, buf1);
    apply_window(buf1, buf2);
    write_output(buf2, out);
}
